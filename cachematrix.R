## This pair of functions will cache the inverse of a matrix instead of computing it repeatedly, which is a costly computation

## This function creates a special "matrix" object that can cache its inverse by using <<- operator.

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinverse <- function(inverse) m <<- inverse
    getinverse <- function() m
    list(set = set, get = get,
         setinverse = setinverse,
         getinverse = getinverse)
}


